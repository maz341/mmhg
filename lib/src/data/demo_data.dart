List<Measurement> generateMeasurements() {
  final List<Measurement> measurements = [];
  int index = 1;
  for (final patient in demoPatients) {
    final random = Random();
    final noMeasurements = random.nextInt(40);
    for (var i = 1; i <= noMeasurements; i++) {
      final type = MeasurementType.values[random.nextInt(2)];
      measurements.add(
        Measurement(
          id: index,
          dateTaken: DateTime.now(),
          type: type,
          value: type == MeasurementType.bloodPressure
              ? random.nextInt(80) + 40.0
              : random.nextInt(60) + 30.0,
          patientId: patient.id,
        ),
      );
      index++;
    }
  }
  return measurements;
}

